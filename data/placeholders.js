const times = () => {
    let arr = [];
    const hours = Array.from({ length: 24 }, (_, i) => `${(i % 12) + 1} ${i < 12 ? 'am' : 'pm'}`);
    for (let i = 0; i < hours.length; i++) {
        const hour = hours[i];
        for (let j = 0; j < timezones.length; j++) {
            const timezone = timezones[j];
            arr.push(`${hour} ${timezone}`)
        }

    }
    return arr;
}

